package fr.unice.polytech.si3.qgl.soyouz.classes.geometry;

/**
 * A Point defined by its abscissa x and ordinate y.
 */
public class Point
{
    private double x;
    private double y;

    /**
     * Getter.
     * @return the abscissa of the Point.
     */
    public double getX()
    {
        return x;
    }

    /**
     * Getter.
     * @return the ordinate of the Point.
     */
    public double getY()
    {
        return y;
    }
}
